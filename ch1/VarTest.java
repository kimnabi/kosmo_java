package a.b.c.ch1;

public class VarTest {

	public static void main(String args[]) {

		// statement 문장, 문장을 종결할 때는 ; 을 사용한다.
		/*
		1. int : 자료형 인트
		2. i : 변수, 변수 이름이 i, 변수는 값을 담는 통(공간)
		3. = : 대입 연산자 : 오른쪽에 있는 값을 왼쪽에 대입한다. 
		4. 1 : 값 : 정수 1 
		5. ; : 문자 종결 연산자(기호) : 세미콜론 
		*/
		/*
		정수 자료형 int 를 선언하고 
		변수 i 에 값 1을 대입했다(초기화 했다).
		*/
		int i = 1;
	
		// statement 문장 
		// 상위에서 선언 한 변수 i 를 호출(invoke)해서 
		// 콘솔에 i 값을 출력 했다.
		System.out.println(i);
	
	} // end of main function

} // end of VarTest class